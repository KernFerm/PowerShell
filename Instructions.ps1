# PowerShell Installation Instructions Script

# Print instructions for downloading the PowerShell installer
Write-Host "Instructions to Download PowerShell Installer:"
Write-Host "1. Visit the official PowerShell GitHub releases page: https://github.com/PowerShell/PowerShell/releases"
Write-Host "2. KernFerm github page: https://github.com/KernFerm/PowerShell"
Write-Host "3. Select the version you wish to install."
Write-Host "4. Download the appropriate MSI file for your system architecture (64-bit or 32-bit)."

# Print instructions for installing PowerShell
Write-Host "`nInstructions for Installing PowerShell:"
Write-Host "1. Navigate to the folder where you downloaded the MSI file."
Write-Host "2. Right-click on the downloaded MSI file and select 'Install'."
Write-Host "3. Follow the on-screen instructions to complete the installation."

# Print instructions for verifying the installation
Write-Host "`nInstructions to Verify PowerShell Installation:"
Write-Host "1. Open PowerShell by searching for it in the Start Menu."
Write-Host "2. In the PowerShell window, type `$PSVersionTable.PSVersion and press Enter."
Write-Host "3. Verify that the version number matches the version you installed."